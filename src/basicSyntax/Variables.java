package basicSyntax;

public class Variables {

	public static void main(String[] args) {
		int num1 = 7;
		double num2 = 3.14;
		System.out.println(num1);
		System.out.println(num2);

		String myFavorite = "私の好きな";
		
		System.out.println(myFavorite + "整数は" + num1 + "です。");
		System.out.println(myFavorite + "小数は" + num2 + "です。");
	}

}
